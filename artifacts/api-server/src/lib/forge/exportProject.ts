import { ZipArchive } from "archiver";
import type { Response } from "express";
import type { ContractProjectRow } from "@workspace/db";

function foundryToml(contractName: string): string {
  return `[profile.default]
src = "src"
out = "out"
libs = ["lib"]
test = "test"
solc_version = "0.8.24"

# Generated for "${contractName}" by AURA Forge.
# Docs: https://book.getfoundry.sh/
`;
}

function hardhatConfig(): string {
  return `require("@nomicfoundation/hardhat-toolbox");

/** @type import('hardhat/config').HardhatUserConfig */
module.exports = {
  solidity: "0.8.24",
  networks: {
    // Fill in your RPC URL + private key to deploy, e.g. via a .env file:
    // sepolia: { url: process.env.RPC_URL, accounts: [process.env.PRIVATE_KEY] },
  },
};
`;
}

function evmDeployScript(contractName: string): string {
  return `// Deploy script (Hardhat). Run with: npx hardhat run scripts/deploy.js --network <network>
async function main() {
  const Factory = await ethers.getContractFactory("${contractName}");
  const contract = await Factory.deploy();
  await contract.waitForDeployment();
  console.log("${contractName} deployed to:", await contract.getAddress());
}

main().catch((err) => {
  console.error(err);
  process.exitCode = 1;
});
`;
}

function evmPackageJson(contractName: string): string {
  return JSON.stringify(
    {
      name: contractName.toLowerCase(),
      version: "1.0.0",
      private: true,
      scripts: {
        build: "npx hardhat compile",
        test: "npx hardhat test",
        deploy: "npx hardhat run scripts/deploy.js",
      },
      devDependencies: {
        hardhat: "^2.22.0",
        "@nomicfoundation/hardhat-toolbox": "^5.0.0",
      },
    },
    null,
    2,
  );
}

function anchorToml(contractName: string): string {
  const programId = "11111111111111111111111111111111";
  const snakeCase = contractName
    .replace(/([a-z0-9])([A-Z])/g, "$1_$2")
    .toLowerCase();
  return `[toolchain]

[features]
seeds = false
skip-lint = false

[programs.localnet]
${snakeCase} = "${programId}"

[registry]
url = "https://api.apr.dev"

[provider]
cluster = "Localnet"
wallet = "~/.config/solana/id.json"

[scripts]
test = "yarn run ts-mocha -p ./tsconfig.json -t 1000000 tests/**/*.ts"
`;
}

function solanaPackageJson(contractName: string): string {
  return JSON.stringify(
    {
      name: contractName.toLowerCase(),
      version: "1.0.0",
      private: true,
      scripts: {
        build: "anchor build",
        test: "anchor test",
        deploy: "anchor deploy",
      },
      devDependencies: {
        "@coral-xyz/anchor": "^0.30.0",
        "ts-mocha": "^10.0.0",
      },
    },
    null,
    2,
  );
}

function solanaDeployScript(): string {
  return `#!/usr/bin/env bash
# Deploy script (Anchor). Run: ./scripts/deploy.sh <cluster>
set -e
CLUSTER="\${1:-devnet}"
anchor deploy --provider.cluster "$CLUSTER"
`;
}

