/**
 * Streams a zip archive containing the contract source, generated tests (if
 * any), ecosystem-standard toolchain boilerplate, a deploy script, and a
 * README directly to the HTTP response.
 */
export function streamProjectExport(project: ContractProjectRow, res: Response): void {
  const isEvm = project.ecosystem === "EVM";
  const fileSafeName = project.contractName.replace(/[^a-zA-Z0-9_-]/g, "") || "Contract";

  res.setHeader("Content-Type", "application/zip");
  res.setHeader(
    "Content-Disposition",
    `attachment; filename="${fileSafeName.toLowerCase()}-export.zip"`,
  );

  const archive = new ZipArchive({ zlib: { level: 9 } });
  archive.on("error", (err: Error) => {
    // Headers are already sent by the time archiver streams data, so just
    // terminate the response; there's no clean way to send a JSON error here.
    res.destroy(err);
  });
  archive.pipe(res);

  archive.append(readme(project), { name: "README.md" });

  if (isEvm) {
    archive.append(project.smartContractCode ?? "", { name: `src/${fileSafeName}.sol` });
    if (project.testSuiteCode) {
      archive.append(project.testSuiteCode, { name: `test/${fileSafeName}.t.sol` });
    }
    archive.append(hardhatConfig(), { name: "hardhat.config.js" });
    archive.append(foundryToml(project.contractName), { name: "foundry.toml" });
    archive.append(evmPackageJson(project.contractName), { name: "package.json" });
    archive.append(evmDeployScript(project.contractName), { name: "scripts/deploy.js" });
    if (project.abiOrIdl) {
      archive.append(project.abiOrIdl, { name: `abi/${fileSafeName}.abi.json` });
    }
  } else {
    const programDir = `programs/${fileSafeName.toLowerCase()}/src`;
    archive.append(project.smartContractCode ?? "", { name: `${programDir}/lib.rs` });
    if (project.testSuiteCode) {
      archive.append(project.testSuiteCode, { name: `tests/${fileSafeName.toLowerCase()}.ts` });
    }
    archive.append(anchorToml(project.contractName), { name: "Anchor.toml" });
    archive.append(solanaPackageJson(project.contractName), { name: "package.json" });
    archive.append(solanaDeployScript(), { name: "scripts/deploy.sh" });
    if (project.abiOrIdl) {
      archive.append(project.abiOrIdl, { name: `idl/${fileSafeName.toLowerCase()}.json` });
    }
  }

  archive.finalize();
}
